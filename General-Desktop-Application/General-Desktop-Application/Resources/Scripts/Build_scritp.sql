CREATE TABLE build_general.[user] (user_uuid uniqueidentifier NOT NULL, user_email nvarchar(max) NULL, user_value nvarchar(max) NULL, user_password nvarchar(max) NOT NULL, user_fullname nvarchar(max) NOT NULL, user_gender bit NOT NULL, user_landline nvarchar(max) NULL, user_cellphonenumber nvarchar(max) NULL, user_preferences nvarchar(max) NULL, user_role tinyint NOT NULL, user_enabled bit NOT NULL, user_maximumhoursperiod tinyint NULL, user_minimumhoursperiod tinyint NULL, city_uuid uniqueidentifier NOT NULL, file_uuid_avatar uniqueidentifier NULL, user_uuid_root uniqueidentifier NULL, sess_uuid_used uniqueidentifier NULL, sess_uuid_created uniqueidentifier NOT NULL, sess_uuid_deleted uniqueidentifier NULL, PRIMARY KEY (user_uuid));
