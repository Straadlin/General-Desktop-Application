CREATE TABLE build_general.appointment (appo_uuid uniqueidentifier NOT NULL, appo_time time(0) NOT NULL, appo_cancel bit DEFAULT 0 NOT NULL, date_uuid uniqueidentifier NOT NULL, brof_uuid uniqueidentifier NOT NULL, appo_uuid_root uniqueidentifier NULL, sess_uuid_used uniqueidentifier NULL, sess_uuid_created uniqueidentifier NOT NULL, sess_uuid_deleted uniqueidentifier NULL, PRIMARY KEY (appo_uuid));
CREATE TABLE build_general.class (clas_uuid uniqueidentifier NOT NULL, clas_folio nvarchar(10) NOT NULL, clas_time time(0) NOT NULL, user_uuid_teacher uniqueidentifier NOT NULL, clro_uuid uniqueidentifier NOT NULL, date_uuid uniqueidentifier NOT NULL, brof_uuid uniqueidentifier NOT NULL, clas_uuid_root uniqueidentifier NULL, sess_uuid_used uniqueidentifier NULL, sess_uuid_created uniqueidentifier NOT NULL, sess_uuid_deleted uniqueidentifier NULL, PRIMARY KEY (clas_uuid));
CREATE TABLE build_general.class_user (clas_uuid uniqueidentifier NOT NULL, user_uuid uniqueidentifier NOT NULL, PRIMARY KEY (clas_uuid, user_uuid));
CREATE TABLE build_general.classroom (clro_uuid uniqueidentifier NOT NULL, clro_value nvarchar(max) NOT NULL, PRIMARY KEY (clro_uuid));
CREATE TABLE build_general.appointment_user (appo_uuid uniqueidentifier NOT NULL, user_uuid uniqueidentifier NOT NULL, PRIMARY KEY (appo_uuid, user_uuid));
CREATE TABLE build_general.kardex (kard_uuid uniqueidentifier NOT NULL, kard_level tinyint NOT NULL, kard_chapter tinyint NOT NULL, kard_type tinyint NOT NULL, kard_description nvarchar(max) NULL, kard_mistake tinyint NULL, user_uuid uniqueidentifier NOT NULL, date_uuid uniqueidentifier NOT NULL, kard_uuid_root uniqueidentifier NULL, sess_uuid_used uniqueidentifier NULL, sess_uuid_created uniqueidentifier NOT NULL, sess_uuid_deleted uniqueidentifier NULL, PRIMARY KEY (kard_uuid));
CREATE TABLE build_general.club (club_uuid uniqueidentifier NOT NULL, club_folio nvarchar(10) NOT NULL, club_time time(0) NOT NULL, user_uuid_teacher uniqueidentifier NOT NULL, date_uuid uniqueidentifier NOT NULL, brof_uuid uniqueidentifier NOT NULL, club_uuid_root uniqueidentifier NULL, sess_uuid_used uniqueidentifier NULL, sess_uuid_created uniqueidentifier NOT NULL, sess_uuid_deleted uniqueidentifier NULL, PRIMARY KEY (club_uuid));
CREATE TABLE build_general.club_user (club_uuid uniqueidentifier NOT NULL, user_uuid uniqueidentifier NOT NULL, PRIMARY KEY (club_uuid, user_uuid));
CREATE TABLE build_general.[file] (file_uuid uniqueidentifier NOT NULL, [file_name] nvarchar(max) NOT NULL, file_extension tinyint NOT NULL, file_description nvarchar(max) NULL, file_value varbinary(max) NOT NULL, PRIMARY KEY (file_uuid));
